                <h1>Historia</h1>
                <div style="padding-bottom: 200px;">
                  <img src="imagenes/historia_molle.jpg" style="float: right; width: 500px;height: 400px; padding: 15px;" />
                  El distrito fue creado mediante Ley No.9690 del 12 de diciembre de 1942, en el primer gobierno del Presidente Manuel Prado Ugarteche, inaugurada popularmente el 24 de junio de ese año, instituyéndose como capital del nuevo distrito, Mollebamba. Su primer alcalde fue D. Florentino Suárez Rea.<br> <br>
                  Lleva el nombre de Juan Espinoza Medrano, en reconocimiento a este escritor nacido en Calcahuso, uno de los anexos. <br> <br>
                  Hoy en día, el distrito de Juan Espinoza Medrano es uno de los 7 distritos de la provincia de Antabamba ubicada en el departamento de Apurímac, bajo la administración del Gobierno regional de Apurímac, en el sur del Perú.<br><br>
                  Limita al norte con el distrito de Sabayno y el distrito de Huaquirca, al oeste con el distrito de Antabamba, al sur con el departamento de Ayacucho y el departamento de Arequipa y al oeste con la provincia de Aymaraes.<br><br>
